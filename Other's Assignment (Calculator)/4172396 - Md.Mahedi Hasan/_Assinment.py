def getAdd(num1,num2):
    return num1 + num2
def getSub(num1,num2):
    return num1 - num2
def getMul(num1,num2):
    return num1 * num2
def getDiv(num1,num2):
    return num1 / num2
def getRim(num1,num2):
    return num1 % num2

a = 10
b = 20
result = getAdd(a,b), getSub(a,b), getMul(a,b), getDiv(a,b), getRim(a,b)
print(result)